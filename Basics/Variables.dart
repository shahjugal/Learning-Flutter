void main() {
  // WAY 1

  // var means it will automatically assign type.

  var name = "jugal";

  print(name);

  // will automatically understand that we are trying to assign string.

  // WAY 2 (Most Recommended.)

  // using typename

  int noOfBalls = 100;

  print(noOfBalls);

  // WAY 3

  // using dynamic we can change it's type on runtime.

  dynamic dialogue = "Love you";

  print(dialogue);

  dialogue = 3000;

  print(dialogue);
}
